// Comparison for Boolean

#include <iostream>

using namespace std;

int main()
{
	// Comparison, Relation Expression
	// True = 1, 
	// False = 0,

	// Less than equal and Greater than equal
	
	int a = 45;
	int b = 35;

	bool total1, total2;

	// less than equal
	total1 = (a <= b);
	cout << total1 << endl;

	// greater than equal
	total2 = (a >= b);
	cout << total2 << endl; 

	cin.get();
	return 0;
}
