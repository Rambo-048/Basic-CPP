// Comparison for Boolean

#include <iostream>

using namespace std;

int main()
{
	// Comparison, Relation Expression
	// True = 1, 
	// False = 0,

	// Less than and Greater than

	int a = 2;
	int b = 1;

	bool total1, total2;

	// Less than <
	total1 = (a < b);
	cout << total1 << endl;

	// Greater than >
	total2 = (a > b);
	cout << total2 << endl;

	cin.get();
	return 0;
}